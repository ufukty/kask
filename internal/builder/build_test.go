func assertfile(t *testing.T, tmp, path string) {
	t.Run(strings.ReplaceAll(path, "/", "\\"), func(t *testing.T) {
		if !check(tmp, path) {
			t.Errorf("assert, file not found: %s", path)
		}
	})
}

