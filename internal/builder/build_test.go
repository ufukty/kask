func readFile(path string) string {
	c, err := os.ReadFile(path)
	if err != nil {
		panic(fmt.Errorf("os.ReadFile: %w", err))
	}
	return string(c)
}

var anchor = regexp.MustCompile(`<a[^>]*>[^<]*</a>`)

